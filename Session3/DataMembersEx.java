public class DataMembersEx
{
	public static void main(String args[])
	{
   
   		  byte a = 10;
   		  int b = 20;
   		  char c = 'S';
   		  boolean d = false;
   		  String msg = "Hi";
   		  float e = 10.1f;

   		  System.out.println(a);
   		  System.out.println(b);
   		  System.out.println(c);
   		  System.out.println(d);
   		  System.out.println(msg);
   		  System.out.println(e);




	}
}